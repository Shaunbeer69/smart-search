const { Sequelize, DataTypes } = require('sequelize');
const sequelize = new Sequelize(
    'smart_search',
    'postgres',
    'postgres',
    {
        host: process.env.DB_HOST,
        dialect: 'postgres'
    }
);

const City = sequelize.define('Cities', {
    id: {
        type: DataTypes.INTEGER,
        primaryKey: true,
        autoIncrement: true
    },
    name: {
        type: DataTypes.STRING,
        allowNull: false
    },
    createdAt: {
        allowNull: false,
        type: Sequelize.DATE
    },
    updatedAt: {
        allowNull: false,
        type: Sequelize.DATE
    }
});


